fn rustc_cmd(src: &str, out: &str) -> Command {
    let mut cmd = Command::new("rustc");
    cmd.args([
        src,
        "--edition=2024",
        "-o",
        out,
        "-D",
        "unused_imports",
        "-D",
        "unused_macros",
    ]);
    cmd
}

fn compiled_module(
    mod_name: &str,
    filename: &str,
    file: syn::File,
    is_main: bool,
) -> gors::compiler::CompiledModule {
    gors::compiler::CompiledModule {
        mod_name: mod_name.to_string(),
        import_path: mod_name.to_string(),
        file,
        filename: filename.to_string(),
        content_hash: String::new(),
        is_main,
        is_stdlib: false,
    }
}

fn generated_prelude() -> &'static str {
    "//! Generated by gors. Do not edit.\n#![deny(dead_code, unused_imports, unused_macros, unsafe_code)]\n#![allow(nonstandard_style)]\n\n"
}

fn write_fixture_file(path: &Path, source: &str) {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).unwrap();
    }
    std::fs::write(path, source).unwrap();
}

fn compile_temp_program(dir: &Path) -> gors::backend_rust::GeneratedOutput {
    let program = gors::parser::parse_program(dir.to_str().unwrap()).unwrap();
    let compiled = gors::compiler::compile_program_multi(program).unwrap();
    gors::backend_rust::generate_multi(compiled).unwrap()
}

#[test]
fn generated_output_orders_macro_rules_before_invocations() {
    let file: syn::File = syn::parse_quote! {
        impl_real_complex_conversions!(i32);

        macro_rules! impl_real_complex_conversions {
            ($($ty:ty),*) => {};
        }
    };
    let source = gors::backend_rust::generate(file).unwrap();
    let definition = source
        .find("macro_rules! impl_real_complex_conversions")
        .unwrap();
    let invocation = source.find("impl_real_complex_conversions!(i32);").unwrap();

    assert!(definition < invocation, "{source}");
}

