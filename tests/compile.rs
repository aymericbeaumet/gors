#[test]
fn compile_sourcemap() {
    let dirs = discover_program_dirs();

    for dir in &dirs {
        let (ast, files) = match gors::parser::parse_path(dir.to_str().unwrap()) {
            Ok(result) => result,
            Err(_) => continue,
        };

        let (go_file, go_source) = match files.first() {
            Some((f, s)) => (f.as_str(), s.as_str()),
            None => continue,
        };

        let compiled = match gors::compiler::compile_with_source_map(ast, go_file, go_source) {
            Ok(compiled) => compiled,
            Err(_) => continue,
        };

        let rust_source = match gors::backend_rust::generate(compiled) {
            Ok(s) => s,
            Err(_) => continue,
        };

        let source_map = gors::compiler::build_source_map(&rust_source);

        let mut buf = Vec::new();
        if source_map.to_writer(&mut buf).is_err() {
            continue;
        }

        let parsed = match sourcemap::SourceMap::from_reader(&buf[..]) {
            Ok(sm) => sm,
            Err(e) => panic!("Invalid sourcemap for {}: {}", go_file, e),
        };

        assert!(
            parsed.get_token_count() > 0,
            "Empty sourcemap for {}",
            go_file
        );
        assert_eq!(
            parsed.get_source(0),
            Some(go_file),
            "Source file mismatch for {}",
            go_file
        );
    }
}

#[test]
fn generate_multi_no_unused_code() {
    let dirs = discover_program_dirs();

    for dir in &dirs {
        let main_go = dir.join("main.go");
        if !main_go.exists() {
            continue;
        }

        let path = dir.to_string_lossy().to_string();
        let program = match gors::parser::parse_program(&path) {
            Ok(p) => p,
            Err(_) => continue,
        };

        let compiled = match gors::compiler::compile_program_multi(program) {
            Ok(c) => c,
            Err(_) => continue,
        };

        let output = match gors::backend_rust::generate_multi(compiled) {
            Ok(o) => o,
            Err(_) => continue,
        };

        let tmp = tempfile::tempdir().unwrap();
        for (filename, source) in &output.files {
            std::fs::write(tmp.path().join(filename), source).unwrap();
        }

        let main_rs = tmp.path().join("main.rs");
        let bin = tmp.path().join("main");

        let baseline = Command::new("rustc")
            .args([
                main_rs.to_str().unwrap(),
                "--edition=2024",
                "-o",
                bin.to_str().unwrap(),
            ])
            .output()
            .unwrap();
        if !baseline.status.success() {
            continue;
        }

        let out = rustc_cmd(main_rs.to_str().unwrap(), bin.to_str().unwrap())
            .output()
            .unwrap();

        let stderr = String::from_utf8_lossy(&out.stderr);
        assert!(
            out.status.success(),
            "generate_multi output for {} failed:\n{}",
            dir.display(),
            stderr,
        );
    }
}

