import { access, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..", "..");
const inputFile = path.join(
	repoRoot,
	"tests",
	"fixtures",
	"conformance",
	"spec.json",
);
const outputFile = path.join(scriptDir, "..", "src", "spec-conformance.ts");
const goProgramRoot = path.join(repoRoot, "tests", "fixtures", "go_programs");

async function pathExists(candidate) {
	try {
		await access(candidate);
		return true;
	} catch {
		return false;
	}
}

function assertString(value, field) {
	if (typeof value !== "string" || value.trim() === "") {
		throw new Error(`${field} must be a non-empty string`);
	}
	return value;
}

async function validateReport(report) {
	if (!report || typeof report !== "object") {
		throw new Error("conformance report must be an object");
	}
	assertString(report.source?.title, "source.title");
	assertString(report.source?.url, "source.url");
	assertString(report.source?.languageVersion, "source.languageVersion");
	if (!Array.isArray(report.categories) || report.categories.length === 0) {
		throw new Error("categories must be a non-empty array");
	}

	const ids = new Set();
	for (const [categoryIndex, category] of report.categories.entries()) {
		const categoryName = assertString(
			category.name,
			`categories[${categoryIndex}].name`,
		);
		if (!Array.isArray(category.tests) || category.tests.length === 0) {
			throw new Error(`${categoryName} must contain at least one test`);
		}

		for (const [testIndex, test] of category.tests.entries()) {
			const prefix = `${categoryName}.tests[${testIndex}]`;
			const id = assertString(test.id, `${prefix}.id`);
			if (ids.has(id)) throw new Error(`duplicate conformance id: ${id}`);
			ids.add(id);
			assertString(test.section, `${prefix}.section`);
			assertString(test.title, `${prefix}.title`);
			if (test.status !== "passing" && test.status !== "skipped") {
				throw new Error(`${id} has invalid status: ${test.status}`);
			}

			const fixtures = Array.isArray(test.fixtures) ? test.fixtures : [];
			if (test.status === "passing") {
				if (fixtures.length === 0) {
					throw new Error(`${id} is passing but has no fixtures`);
				}
				for (const fixture of fixtures) {
					assertString(fixture, `${id}.fixtures[]`);
					const mainGo = path.join(goProgramRoot, fixture, "main.go");
					if (!(await pathExists(mainGo))) {
						throw new Error(`${id} references missing fixture ${fixture}`);
					}
				}
			} else {
				assertString(test.reason, `${id}.reason`);
			}
		}
	}
}

function buildReport(sourceReport) {
	const categories = sourceReport.categories.map((category) => {
		const tests = category.tests.map((test) => ({
			id: test.id,
			section: test.section,
			title: test.title,
			status: test.status,
			fixtures: test.fixtures ?? [],
			reason: test.reason ?? "",
		}));
		const passingTestCount = tests.filter(
			(test) => test.status === "passing",
		).length;
		return {
			name: category.name,
			testCount: tests.length,
			passingTestCount,
			skippedTestCount: tests.length - passingTestCount,
			tests,
		};
	});

	const testCount = categories.reduce(
		(sum, category) => sum + category.testCount,
		0,
	);
	const passingTestCount = categories.reduce(
		(sum, category) => sum + category.passingTestCount,
		0,
	);
	const skippedTestCount = testCount - passingTestCount;
	const passingCategoryCount = categories.filter(
		(category) => category.skippedTestCount === 0,
	).length;

	return {
		source: sourceReport.source,
		categories,
		summary: {
			categoryCount: categories.length,
			passingCategoryCount,
			testCount,
			passingTestCount,
			skippedTestCount,
		},
	};
}

function emitTypescript(report) {
	return `// Generated by scripts/generate-conformance-report.mjs.
// Run \`npm run generate:conformance-report\` from www/ after editing tests/fixtures/conformance/spec.json.

export type SpecConformanceStatus = "passing" | "skipped";

export interface SpecConformanceSource {
\treadonly title: string;
\treadonly url: string;
\treadonly languageVersion: string;
\treadonly published: string;
\treadonly retrieved: string;
}

export interface SpecConformanceTest {
\treadonly id: string;
\treadonly section: string;
\treadonly title: string;
\treadonly status: SpecConformanceStatus;
\treadonly fixtures: readonly string[];
\treadonly reason: string;
}

export interface SpecConformanceCategory {
\treadonly name: string;
\treadonly testCount: number;
\treadonly passingTestCount: number;
\treadonly skippedTestCount: number;
\treadonly tests: readonly SpecConformanceTest[];
}

export const specConformanceSource: SpecConformanceSource = ${JSON.stringify(report.source, null, "\t")};

export const specConformanceCategories: readonly SpecConformanceCategory[] = ${JSON.stringify(report.categories, null, "\t")};

export const specConformanceSummary = ${JSON.stringify(report.summary, null, "\t")} as const;
`;
}

async function main() {
	const sourceReport = JSON.parse(await readFile(inputFile, "utf8"));
	await validateReport(sourceReport);
	const report = buildReport(sourceReport);
	await writeFile(outputFile, emitTypescript(report));
	console.log(`Wrote ${path.relative(repoRoot, outputFile)}`);
}

main().catch((error) => {
	console.error(error);
	process.exitCode = 1;
});
